struct Solution;

impl Solution {
    /// [3266. K 次乘运算后的最终数组 II](https://leetcode.cn/problems/final-array-state-after-k-multiplication-operations-ii/)
    pub fn get_final_state(nums: Vec<i32>, k: i32, multiplier: i32) -> Vec<i32> {
        #[inline]
        fn binpow(mut num: i64, mut exp: i64) -> i64 {
            let mut pow = 1;
            while exp > 0 {
                if exp & 1 == 1 {
                    pow = pow * num % M;
                }
                num = num * num % M;
                exp >>= 1;
            }

            pow
        }

        if multiplier == 1 {
            return nums;
        }

        const M: i64 = 1_000_000_007;
        let (len, max) = (
            nums.len(),
            if let Some(&max) = nums.iter().max() {
                max as i64
            } else {
                0
            },
        );

        let mut heap = std::collections::BinaryHeap::from_iter(
            nums.into_iter()
                .enumerate()
                .map(|(i, n)| std::cmp::Reverse((n as i64, i))),
        );

        let mut k = k as i64;
        while let Some(std::cmp::Reverse((n, i))) = heap.pop() {
            if k == 0 || n >= max {
                heap.push(std::cmp::Reverse((n, i)));
                break;
            }
            heap.push(std::cmp::Reverse((n * multiplier as i64, i)));
            k -= 1;
        }

        let mut nums = vec![0; len];
        for (j, std::cmp::Reverse((n, i))) in heap.into_sorted_vec().into_iter().rev().enumerate() {
            nums[i] = ((n % M)
                * binpow(
                    multiplier as i64,
                    k / len as i64 + if (j as i64) < k % len as i64 { 1 } else { 0 },
                )
                % M) as i32;
        }

        nums
        // 34ms/2.49MB
    }
}

