struct Solution;

impl Solution {
    /// [491. 非递减子序列](https://leetcode.cn/problems/non-decreasing-subsequences/)
    pub fn find_subsequences(nums: Vec<i32>) -> Vec<Vec<i32>> {
        fn bt(nums: &[i32], i: usize, sub: &mut Vec<i32>) -> Vec<Vec<i32>> {
            let mut seq = Vec::new();
            if sub.len() > 1 {
                seq.push(sub.clone());
            }

            let mut set = std::collections::HashSet::new();
            for j in i..nums.len() {
                if nums[j]
                    < if let Some(&last) = sub.last() {
                        last
                    } else {
                        i32::MIN
                    }
                    || set.contains(&nums[j])
                {
                    continue;
                }
                set.insert(nums[j]);
                sub.push(nums[j]);
                seq.append(&mut bt(nums, j + 1, sub));
                sub.pop();
            }

            seq
        }

        bt(&nums, 0, &mut Vec::new())
        // 7ms/5.02MB
    }
}

